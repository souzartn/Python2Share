#Plus: Adds two values
resultA = 1 + 2
print('Plus: {0}'.format(resultA))

#Minus: Subtracts two values
resultB = 10 - 2.2
print('Minus: {0}'.format(resultB))

#Multiply
x = 5 * 2
print('Multiply: {0}'.format(x))

#Divide 
x = 10 / 2
print('Divide: {0}'.format(x))

#Power:  22
x = 2 ** 2
print('Power: {0}'.format(x))

#Greater than: Returns whether first number is greater than the second number, returns the boolean value True
trueOrFalse = 3 > 2
print('Greater than: {0}'.format(trueOrFalse))

#Less than: Returns whether first number is less than the second number, returns the boolean value False
trueOrFalse = 3 < 2
print('Less than: {0}'.format(trueOrFalse))

#Greater than or equal to; expression below returns True
trueOrFalse = 3 >= 3
print('Greater than or equal to: {0}'.format(trueOrFalse))

# Less than or equal to; expression below returns True
trueOrFalse = 3 <= 3
print('Less than or equal to: {0}'.format(trueOrFalse))

# equal to; expression below returns False
trueOrFalse = 2 == 3
print('equal to: {0}'.format(trueOrFalse))

# not equal to; expression below returns True
trueOrFalse = 2 != 3
print('not equal to: {0}'.format(trueOrFalse))

# boolean AND; expression below returns False
trueOrFalse = (5 == 3) and (2 != 2)
print('boolean AND: {0}'.format(trueOrFalse))

# boolean AND; expression below returns True
trueOrFalse = (3 != 4) or (5 == 5)
print('boolean AND: {0}'.format(trueOrFalse))